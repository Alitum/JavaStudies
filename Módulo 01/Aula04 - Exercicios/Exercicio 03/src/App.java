public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("[Aula 04] Exercício 03 - While");

        System.out.println("Contagem iniciada");
        int number = 10;
        while (number > 0) {
            System.out.println(number);
            number--;
        }

        System.out.println("Contagem finalizada");
    }
}
